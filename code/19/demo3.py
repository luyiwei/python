num_one = float(input("第一个:"))
operator = input("运算符:")
num_two = float(input("第二个:"))
if operator == "+":
    print(num_one + num_two)
elif operator == "-":
    print(num_one - num_two)
elif operator == "*":
    print(num_one + num_two)
elif operator == "/":
    print(num_one + num_two)
else:
    print("错误")

